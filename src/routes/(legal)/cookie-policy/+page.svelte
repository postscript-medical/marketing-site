<h2>
  Categories of cookies
</h2>

<p>
  <i>Strictly necessary cookies</i>. These are cookies that are required for the operation of our website. They include, for example, cookies that enable you to log into secure areas of each of our websites, to use a shopping cart or to use our services.
</p>

<p>
  <i>Analytical/performance cookies</i>. They allow us to recognise and count the number of visitors and to see how visitors move around our website when they are using it. This helps us to improve the way in which our website works, for example, by ensuring that users are finding what they are looking for easily.
</p>

<p>
